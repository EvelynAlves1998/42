int	ft_strcmp(char *s1, char *s2)
{
	char	*p1;
	char	*p2;
	int		i;

	i = 0;
	p1 = s1;
	p2 = s2;
	while ((p1[i] && p2[i] != '\0') && (p1[i] == p2[i]))
	{
		p1++;
		p2++;
	}
	return ((unsigned char)p1[i] - (unsigned char)p2[i]);
}
