char	*ft_strupcase(char *str)
{
	char		*p;
	int			i;

	p = str;
	i = 0;
	while (p[i] != '\0')
	{
		if (p[i] > 96 && p[i] < 123)
		{
			p[i] -= 32;
		}
		i++;
	}
	return (str);
}
