void	ft_ultimate_div_mod(int *a, int *b)
{
	int a1;
	int b1;
	int d;
	int m;

	a1 = *a;
	b1 = *b;
	d = a1 / b1;
	m = a1 % b1;
	*a = d;
	*b = m;
}
