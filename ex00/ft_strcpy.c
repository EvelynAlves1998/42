char	*ft_strcpy(char *dest, char *src)
{
	char	*p;
	int		i;

	p = src;
	i = 0;
	while (p[i] != '\0')
	{
		dest[i] = p[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
