void	ft_rev_int_tab(int *tab, int size)
{
	int		m;
	int		s;

	m = 0;
	while (m < size)
	{
		s = tab[m];
		tab[m] = tab[size - 1];
		tab[size - 1] = s;
		m++;
		size--;
	}
}
