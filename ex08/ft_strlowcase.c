char	*ft_strlowcase(char *str)
{
	char		*p;
	int			i;

	p = str;
	i = 0;
	while (p[i] != '\0')
	{
		if (p[i] > 64 && p[i] < 91)
		{
			p[i] += 32;
		}
		i++;
	}
	return (str);
}
