#include <unistd.h>

void	ft_strlen(char *str)
{
	int		i;
	char	*cstr;

	i = 0;
	cstr = str;
	while (*cstr != 0)
	{
		write(1, cstr, 1);
		i++;
		cstr++;
	}
}
