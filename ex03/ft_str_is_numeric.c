int		ft_str_is_numeric(char *str)
{
	char	*p;
	int		i;

	p = str;
	i = 0;
	while (p[i] != '\0')
	{
		if (p[i] >= '0' && p[i] <= '9')
		{
			i++;
		}
		else
		{
			return (0);
		}
	}
	return (1);
}
